-- ----------------------------------------------------------------
-- '그만' 등 명시적 수신 중단 요청은 availability='휴면'(소프트 상태 — 선별 재컨택으로
-- 복구 가능)과 다른 컴플라이언스 상태다. 이 값이 기록된 지원자는 캠페인성 발송
-- (벌크 발송·디스패치)에서 영구 제외한다. 지원자가 먼저 보낸 문자에 대한
-- 1:1 응대(에이전트·매니저)는 수신거부 대상이 아니므로 제한하지 않는다.
--
-- 재실행 안전: 멱등 (IF NOT EXISTS).

ALTER TABLE applicants
  ADD COLUMN IF NOT EXISTS sms_opt_out_at TIMESTAMPTZ;
